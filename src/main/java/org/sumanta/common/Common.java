package org.sumanta.common;

public class Common {

	public static String source;
	public static String destination;
	public static String iporhostname;
	public static String user;
	public static String passwd;
	
	public static String getSource() {
		return source;
	}
	public static void setSource(String s) {
		source = s;
	}
	public static String getDestination() {
		return destination;
	}
	public static void setDestination(String d) {
		destination = d;
	}
	public static String getIporhostname() {
		return iporhostname;
	}
	public static void setIporhostname(String iporhostname) {
		Common.iporhostname = iporhostname;
	}
	public static String getUser() {
		return user;
	}
	public static void setUser(String user) {
		Common.user = user;
	}
	public static String getPasswd() {
		return passwd;
	}
	public static void setPasswd(String passwd) {
		Common.passwd = passwd;
	}
	
	
}
